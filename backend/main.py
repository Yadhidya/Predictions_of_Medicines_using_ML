# main.py
"""
FastAPI server for Drug Consumption Prediction
Uses artifacts generated by temp_ml.py (RandomForestRegressor + seasonal adjustments)

Endpoints:
    POST /predict_sales         → Predict total & per-product sales
    POST /seasonality_analysis  → Predict month-wise trend for product_class
"""

from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel
import joblib, os, json
import numpy as np
import pandas as pd
from pathlib import Path

MODEL_DIR = Path("temp_ml_model")
if not MODEL_DIR.exists():
    raise RuntimeError("⚠️ temp_ml_model not found. Run temp_ml.py first.")

rf_pipeline = joblib.load(MODEL_DIR / "rf_pipeline.joblib")
metadata = joblib.load(MODEL_DIR / "rf_metadata.joblib")
feature_columns = metadata["feature_columns"]

with open(MODEL_DIR / "historical_totals.json") as f:
    historical_totals = json.load(f)
with open(MODEL_DIR / "product_shares.json") as f:
    product_shares = json.load(f)
with open(MODEL_DIR / "month_factors.json") as f:
    month_factors = json.load(f)
with open(MODEL_DIR / "city_productclass_avg_sales.json") as f:
    city_avg_sales = json.load(f)

productclass_product_map = {}
for city, pcs in product_shares.items():
    for pc, products in pcs.items():
        if pc not in productclass_product_map:
            productclass_product_map[pc] = list(products.keys())

# Disease mapping
DISEASE_MAP = {
    "Analgesics": "Pain-related Disorders",
    "Antibiotics": "Bacterial Infections",
    "Antimalarial": "Parasitic Diseases (Malaria)",
    "Antipiretics": "Fever-related Illnesses",
    "Antiseptics": "Infection Prevention & Wound Care",
    "Mood Stabilizers": "Mental Health Disorders"
}

MONTH_TO_SEASON = {
    1:"Winter",2:"Winter",3:"Spring",4:"Spring",5:"Summer",6:"Summer",
    7:"Monsoon",8:"Monsoon",9:"Autumn",10:"Autumn",11:"Winter",12:"Winter"
}

# ================== FastAPI App ==================
app = FastAPI(title="Drug Consumption Prediction API")
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"]
)

# ================== Data Model ==================
class SalesRequest(BaseModel):
    country: str
    city: str
    product_class: str
    month: int
    year: int

def cyclic_month(month: int):
    """Compute cyclic encoding for month."""
    return float(np.sin(2*np.pi*month/12)), float(np.cos(2*np.pi*month/12))

def get_month_factor(city, pc, month):
    try:
        return float(month_factors.get(city, {}).get(pc, {}).get(str(month), 1.0))
    except:
        return 1.0

def get_historical_total(city, pc, month):
    """Fetch average total sales for (city, product_class, month)"""
    try:
        val = historical_totals.get(city, {}).get(pc, {}).get(str(month))
        if val is not None:
            return float(val)
        d = historical_totals.get(city, {}).get(pc, {})
        if d:
            return float(np.mean(list(map(float, d.values()))))
    except:
        pass
    try:
        return float(city_avg_sales.get(city, {}).get(pc, 0) or 0)
    except:
        return 0.0

def get_product_share(city, pc, product):
    try:
        val = product_shares.get(city, {}).get(pc, {}).get(product)
        return float(val) if val is not None else None
    except:
        return None

def prepare_input_row(product: str, req: SalesRequest, month_override=None):
    """Build input DataFrame for the model."""
    month = month_override if month_override else req.month
    month_sin, month_cos = cyclic_month(month)
    season = MONTH_TO_SEASON.get(month, "Winter")

    row = {
        "country": req.country,
        "city": req.city,
        "product_class": req.product_class,
        "product_name": product,
        "month": month,
        "year": req.year,
        "quantity": np.log1p(10),
        "price": 100,
        "channel": "Retail",
        "sub-channel": "Pharmacy",
        "season": season,
        "month_sin": month_sin,
        "month_cos": month_cos,
        "productclass_month": f"{req.product_class}_{month}",
        "pc_sales_lag1_log": 0.0,
        "pc_sales_roll3_log": 0.0
    }
    df = pd.DataFrame([row])
    for c in feature_columns:
        if c not in df.columns:
            df[c] = 0
    return df[feature_columns]

# ================== /predict_sales Endpoint ==================
@app.post("/predict_sales")
def predict_sales(req: SalesRequest):
    try:
        pc = req.product_class
        city = req.city
        if pc not in productclass_product_map:
            raise HTTPException(status_code=400, detail=f"Product class '{pc}' not found.")

        meds = productclass_product_map[pc]
        raw_preds = []

        for med in meds:
            X = prepare_input_row(med, req)
            hist_total = get_historical_total(city, pc, req.month)
            share = get_product_share(city, pc, med)

            if share and hist_total > 0:
                val = hist_total * share
                if val is None or np.isnan(val) or val <= 0:
                    val = 0.0
                inferred = np.log1p(val)
                X["pc_sales_lag1_log"] = inferred
                X["pc_sales_roll3_log"] = inferred

            pred_log = float(rf_pipeline.predict(X)[0])
            pred = float(np.expm1(pred_log))
            raw_preds.append((med, max(pred, 0.0)))

        sum_raw = sum(v for _, v in raw_preds) or 1.0

        hist_total = get_historical_total(city, pc, req.month)
        month_factor = get_month_factor(city, pc, req.month)
        hist_est = hist_total * month_factor
        final_total = 0.85 * hist_est + 0.15 * sum_raw if hist_est > 0 else sum_raw

        scaled = [(m, round(final_total * (v / sum_raw), 2)) for m, v in raw_preds]
        total_pred = round(sum(v for _, v in scaled), 2)
        avg_pred = round(total_pred / max(1, len(scaled)), 2)

        city_avg = float(city_avg_sales.get(city, {}).get(pc, 0) or 0)
        ratio = avg_pred / (city_avg + 1e-6)
        if ratio > 1.5:
            outbreak = "🚨 High Risk of Outbreak"
        elif ratio > 1.2:
            outbreak = "⚠️ Moderate Risk"
        else:
            outbreak = "🟢 Low Risk"

        return {
            "country": req.country,
            "city": req.city,
            "product_class": req.product_class,
            "disease_category": DISEASE_MAP.get(req.product_class, "General"),
            "month": req.month,
            "year": req.year,
            "total_predicted_sales": total_pred,
            "average_sales_per_product": avg_pred,
            "historical_city_average": round(city_avg, 2),
            "outbreak_alert": outbreak,
            "predictions": [{"product_name": m, "predicted_sales": v} for m, v in scaled]
        }

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

# ================== /seasonality_analysis Endpoint ==================
@app.post("/seasonality_analysis")
def seasonality_analysis(req: SalesRequest):
    try:
        pc = req.product_class
        city = req.city
        if pc not in productclass_product_map:
            raise HTTPException(status_code=400, detail=f"Product class '{pc}' not found.")

        meds = productclass_product_map[pc]
        monthwise = []

        for m in range(1, 13):
            raw_sum = 0.0
            for med in meds:
                X = prepare_input_row(med, req, month_override=m)
                hist_total_m = get_historical_total(city, pc, m)
                share = get_product_share(city, pc, med)

                if share and hist_total_m > 0:
                    val = hist_total_m * share
                    if val is None or np.isnan(val) or val <= 0:
                        val = 0.0
                    inferred = np.log1p(val)
                    X["pc_sales_lag1_log"] = inferred
                    X["pc_sales_roll3_log"] = inferred

                pred_log = float(rf_pipeline.predict(X)[0])
                pred = float(np.expm1(pred_log))
                raw_sum += max(pred, 0.0)

            hist_total = get_historical_total(city, pc, m)
            month_factor = get_month_factor(city, pc, m)
            hist_est = hist_total * month_factor
            final_total = 0.85 * hist_est + 0.15 * raw_sum if hist_est > 0 else raw_sum

            monthwise.append({
                "month": m,
                "season": MONTH_TO_SEASON.get(m, "Unknown"),
                "predicted_sales_total": round(final_total, 2)
            })

        return {
            "country": req.country,
            "city": req.city,
            "product_class": req.product_class,
            "disease_category": DISEASE_MAP.get(req.product_class, "General"),
            "year": req.year,
            "seasonality": monthwise
        }

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

# ================== Run Locally ==================
if __name__ == "__main__":
    import uvicorn
    uvicorn.run("main:app", host="0.0.0.0", port=8000, reload=True)
